		/// <summary>
		/// 保存数据
		/// </summary>
		public static bool SaveEntity(go_tuankeepEntity entity, bool isAdd, DbServers.DbServerName currDbName = DbServers.DbServerName.LatestDB)
		{
			try
			{
				string execSql = (isAdd) ?
				"Insert Into go_tuankeep(tId,uid,addtime)values(@tId,@uid,@addtime)" :
				"Update go_tuankeep Set tId=@tId,uid=@uid,addtime=@addtime Where id=@id";
				SqlParameter[] para = new SqlParameter[]
				{
					new SqlParameter("@id",entity.Id),
					new SqlParameter("@tId",entity.TId),
					new SqlParameter("@uid",entity.Uid),
					(entity.Addtime==null || entity.Addtime==DateTime.MinValue)?new SqlParameter("@addtime",DBNull.Value):new SqlParameter("@addtime",entity.Addtime),
				};
				DataAccessFactory.GetDataProvider(DbServers.GetCurrentDB(currDbName)).Execute(execSql, para);
				return true;
			}
			catch
			{
				return false;
			}
		}

