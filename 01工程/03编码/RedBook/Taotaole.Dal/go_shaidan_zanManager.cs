		public static void Del(int ID, DbServers.DbServerName currDbName = DbServers.DbServerName.LatestDB)
		{
			string deleteSql = string.Format(@"Delete From go_shaidan_zan Where id={0}", ID);
			DataAccessFactory.GetDataProvider(DbServers.GetCurrentDB(currDbName)).Execute(deleteSql);
		}

		/// <summary>
		/// 根据条件删除
		/// </summary>
		public static void DelListData(string where = null, DbServers.DbServerName currDbName = DbServers.DbServerName.LatestDB)
		{
			if (!string.IsNullOrEmpty(where)) where = " Where " + where;
			string deleteSql = string.Format(@"Delete From go_shaidan_zan {0}", where);
			DataAccessFactory.GetDataProvider(DbServers.GetCurrentDB(currDbName)).Execute(deleteSql);
		}

		/// <summary>
		/// 保存数据
		/// </summary>
		public static bool SaveEntity(go_shaidan_zanEntity entity, bool isAdd, DbServers.DbServerName currDbName = DbServers.DbServerName.LatestDB)
		{
			try
			{
				string execSql = (isAdd) ?
				"Insert Into go_shaidan_zan(sd_id,sdhf_userid,start,counts,sdhf_time)values(@sd_id,@sdhf_userid,@start,@counts,@sdhf_time)" :
				"Update go_shaidan_zan Set sd_id=@sd_id,sdhf_userid=@sdhf_userid,start=@start,counts=@counts,sdhf_time=@sdhf_time Where id=@id";
				SqlParameter[] para = new SqlParameter[]
				{
					new SqlParameter("@id",entity.Id),
					new SqlParameter("@sd_id",entity.Sd_id),
					new SqlParameter("@sdhf_userid",entity.Sdhf_userid),
					new SqlParameter("@start",entity.Start),
					new SqlParameter("@counts",entity.Counts),
					(entity.Sdhf_time==null || entity.Sdhf_time==DateTime.MinValue)?new SqlParameter("@sdhf_time",DBNull.Value):new SqlParameter("@sdhf_time",entity.Sdhf_time),
				};
				DataAccessFactory.GetDataProvider(DbServers.GetCurrentDB(currDbName)).Execute(execSql, para);
				return true;
			}
			catch
			{
				return false;
			}
		}

	}
